export function getWordOfDay(words, wordStatus = {}) {
  // Exclui palavras já marcadas como "Já sei" da rotação diária
  const pool = words.filter((w) => wordStatus[w.id] !== 'known')
  const activePool = pool.length > 0 ? pool : words // fallback: se tudo for known, reusa todas

  const dateStr = getTodayStr()
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash << 5) - hash + dateStr.charCodeAt(i)
    hash |= 0
  }
  const index = Math.abs(hash) % activePool.length
  return { word: activePool[index], dateStr, remaining: pool.length }
}

export function getTodayStr() {
  const now = new Date()
  return `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
}

export function getYesterdayStr() {
  const now = new Date()
  now.setDate(now.getDate() - 1)
  return `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
}

function quizHash(value) {
  let hash = 0
  for (let i = 0; i < value.length; i++) {
    hash = (hash << 5) - hash + value.charCodeAt(i)
    hash |= 0
  }
  return Math.abs(hash)
}

function stableQuizShuffle(items, seed) {
  return [...items]
    .map((item, index) => ({
      item,
      sortKey: quizHash(`${seed}-${item.id ?? item.word ?? item}-${index}`),
    }))
    .sort((a, b) => a.sortKey - b.sortKey)
    .map(({ item }) => item)
}

function trimQuizPrompt(text) {
  if (!text) return ''

  const normalized = text.replace(/^["“]|["”]$/g, '').trim()
  if (normalized.length <= 170) return normalized

  const firstSentence = normalized.split('. ')[0]?.trim()
  if (firstSentence && firstSentence.length >= 80) {
    return firstSentence.endsWith('.') ? firstSentence : `${firstSentence}.`
  }

  return `${normalized.slice(0, 167).trimEnd()}...`
}

export function getStudyWordOfDay(words, wordStatus = {}) {
  const pendingPool = words.filter((word) => wordStatus[word.id] !== 'known')
  const reviewPool = pendingPool.filter((word) => wordStatus[word.id] === 'review')
  const activePool =
    reviewPool.length > 0 ? reviewPool :
    pendingPool.length > 0 ? pendingPool :
    words

  const dateStr = getTodayStr()
  const index = quizHash(dateStr) % activePool.length

  return {
    word: activePool[index],
    dateStr,
    remaining: pendingPool.length,
    reviewCount: reviewPool.length,
    source:
      reviewPool.length > 0 ? 'review' :
      pendingPool.length > 0 ? 'fresh' :
      'complete',
  }
}

export function getQuizQuestion(words, word) {
  if (!word) return null

  const distractors = stableQuizShuffle(
    words.filter((candidate) => candidate.id !== word.id),
    `quiz-distractors-${word.id}`
  ).slice(0, 3)

  const options = stableQuizShuffle(
    [word, ...distractors],
    `quiz-options-${word.id}`
  ).slice(0, 4)

  return {
    question:
      word.category === 'sociologia'
        ? 'Qual conceito combina com a definição abaixo?'
        : 'Qual palavra combina com a definição abaixo?',
    prompt: trimQuizPrompt(word.definition),
    options: options.map((option, index) => ({
      id: option.id,
      label: option.word,
      letter: ['A', 'B', 'C', 'D'][index],
      isCorrect: option.id === word.id,
    })),
    correctId: word.id,
  }
}

